//------------Modal Pop up open and close buttona------------//

// Auto Flight plan-Portfolio
document.getElementById("AutoFP").addEventListener('click', function(){
  document.querySelector('.autoFP-bg-modal').style.display = 'flex';
  document.querySelector('.glider-prev').style.visibility = 'hidden';
  document.querySelector('.glider-next').style.visibility = 'hidden';
});

// Close
document.getElementById("AutoFP-close").addEventListener('click', function(){
  document.querySelector('.autoFP-bg-modal').style.display = 'none';
  document.querySelector('.glider-prev').style.visibility = 'visible';
  document.querySelector('.glider-next').style.visibility = 'visible';
});

